public class Nestedfor {

    public static void main(String[] args) {
        int a, b;
        for(a  =  1;  a <=15; a++)
                {
                    System.out.println();
                for(b =  a; b <= 15; b++ )
                {
                    System.out.print (a);
                }
                }
        System.out.println("/n");
    }
}
